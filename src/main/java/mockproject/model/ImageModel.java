package mockproject.model;

public class ImageModel {
	private int id;
	private int filmid;
	private String path;
	
	public ImageModel() {
		super();
	}

	public int getId() {
		return id;
	}

	public int getFilmid() {
		return filmid;
	}

	public String getPath() {
		return path;
	}
	

	public void setId(int id) {
		this.id = id;
	}

	public void setFilmid(int filmid) {
		this.filmid = filmid;
	}

	public void setPath(String path) {
		this.path = path;
	}
	
	
	
	
	
}
